public class Exercicio05_easy {

   public int soma(int numeroDigitado1, int numeroDigitado2, int numeroDigitado3) {
       int soma;
       soma = numeroDigitado1 + numeroDigitado2 + numeroDigitado3;
       return soma;

    }
    public int subtracao(int numeroDigitado1, int numeroDigitado2, int numeroDigitado3) {
        int subtracao;
        subtracao = numeroDigitado1 - numeroDigitado2 - numeroDigitado3; 
        return subtracao;
 
     }
     public int multiplicacao(int numeroDigitado1, int numeroDigitado2, int numeroDigitado3) {
        int multiplicacao;
        multiplicacao = numeroDigitado1 * numeroDigitado2 *numeroDigitado3;
        return multiplicacao;
 
     }
     public int media(int numeroDigitado1, int numeroDigitado2, int numeroDigitado3) {
        int media;
        media = (numeroDigitado1 + numeroDigitado2 + numeroDigitado3)/3;
        return media;
 
     }
}